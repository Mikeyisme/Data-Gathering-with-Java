

public class WeatherDetailsDto {
	private float dataPrecipitation;
	private String fullDate;
	private int month;
	private int week;
	private int year;
	private String stationCity;
	private String stationCode;
	private String stationLocation;
	private String stationState;
	private int averageTemperature;
	private int maximumTemperature;
	private int minimumTemperature;
	private int windDirection;
	private float windSpeed;

	public float getDataPrecipitation() {
		return dataPrecipitation;
	}

	public void setDataPrecipitation(float dataPrecipitation) {
		this.dataPrecipitation = dataPrecipitation;
	}

	public String getFullDate() {
		return fullDate;
	}

	public void setFullDate(String fullDate) {
		this.fullDate = fullDate;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getWeek() {
		return week;
	}

	public void setWeek(int week) {
		this.week = week;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getStationCity() {
		return stationCity;
	}

	public void setStationCity(String stationCity) {
		this.stationCity = stationCity;
	}

	public String getStationCode() {
		return stationCode;
	}

	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}

	public String getStationLocation() {
		return stationLocation;
	}

	public void setStationLocation(String stationLocation) {
		this.stationLocation = stationLocation;
	}

	public String getStationState() {
		return stationState;
	}

	public void setStationState(String stationState) {
		this.stationState = stationState;
	}

	public int getAverageTemperature() {
		return averageTemperature;
	}

	public void setAverageTemperature(int averageTemperature) {
		this.averageTemperature = averageTemperature;
	}

	public int getMaximumTemperature() {
		return maximumTemperature;
	}

	public void setMaximumTemperature(int maximumTemperature) {
		this.maximumTemperature = maximumTemperature;
	}

	public int getMinimumTemperature() {
		return minimumTemperature;
	}

	public void setMinimumTemperature(int minimumTemperature) {
		this.minimumTemperature = minimumTemperature;
	}

	public int getWindDirection() {
		return windDirection;
	}

	public void setWindDirection(int windDirection) {
		this.windDirection = windDirection;
	}

	public float getWindSpeed() {
		return windSpeed;
	}

	public void setWindSpeed(float windSpeed) {
		this.windSpeed = windSpeed;
	}

}
